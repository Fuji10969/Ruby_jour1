# {} permet ici d'insérer des calculs d'integer dans le texte  




puts "On va compter le nombre d'heures de travail à THP" #texte
puts "Travail : #{10 * 5 * 11}" #texte + calcul pour donner le nombres d'heure de travail à THP
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" #texte + calcul pour convertir les heures de travail en minutes

puts "Et en secondes ?" #texte

puts 10 * 5 * 11 * 60 * 60 #ligne de calcul

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #texte question avec des chiffres

puts 3 + 2 < 5 - 7 #texte comprenant des chiffres

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #ligne de texte avec calcul
puts "Ça fait combien 5 - 7 ? #{5 - 7}" #ligne de texte avec calcul

puts "Ok, c'est faux alors !" #ligne de texte

puts "C'est drôle ça, faisons-en plus :" #ligne de texte

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" #Texte avec booléen 
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" #Texte avec booléen 
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" #Texte avec booléen 